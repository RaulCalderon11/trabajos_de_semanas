         private void inicializarElemtos() {
             listRenderer = new DefaultListCellRenderer();
             
             listRenderer.setHorizontalAlignment(DefaultListCellRenderer.CENTER);
             cboTurno.setRenderer(listRenderer);
             this.cboTurno.addItem("seleccione");
             this.cboTurno.addItem("MaÃ±ana");
              this.cboTurno.addItem("Noche");
         }
         
         void ingresarDatos(){
             turno = cboTurno.getSelectedIndex();
             cantidad = Double.parseDouble(txtCantidad.getText());
         }
         
         void calcularPensionActual(){
             switch (turno){
                 case 1:
                     precio = 37.5;
                     break;
                 default:
                     precio = 37.5;
                     break;
             }
         }
         
         void calcularDescuento(){
             if (cantidad >=15 ){
                 descuento = precio*  0.8;
             }
             else if (cantidad < 15){
                 descuento = precio* 0.5;
             }
       
         }
         
         void calcularNuevaPension(){
             totalP = precio - descuento;
         }
         
         void calcularCaramelo(){
             if (totalP> 200){
                 Ccaramelo = cantidad *2;
             } else if(totalP <=200){
                 Ccaramelo = 0;
             }
         }
                
         void mostrarResultados(){
             this.txtSalida.setText("");
             imprimir("");
             imprimir("turno:"+ turno);
             imprimir("Descuento: s/. : "+ descuento);
             imprimir("total a pagar: s/. "+ totalP);
         }
          void imprimir (String cad){
             this.txtSalida.append( cad + "\n");
         }

